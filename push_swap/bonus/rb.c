#include "push_swap_bonus.h"

void	rb(t_list **b)
{
	t_list	*temp;

	if (!(*b) || !(*b)->next)
		return ;
	temp = (*b);
	(*b) = (*b)->next;
	temp->next = NULL;
	lstadd_back(b, temp);
}
